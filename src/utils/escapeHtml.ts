/**
 * Escape HTML special characters to prevent injection in HTML templates.
 * Use this for ALL user-supplied values interpolated into HTML strings.
 */
export function escapeHtml(value: unknown): string {
  const str = String(value ?? '');
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
